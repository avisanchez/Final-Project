    public void drawMinimap(Graphics g, GameObject... players) {
        int cellSize = (int) (Settings.MINIMAP_SCALE * Settings.CELL_SIZE);

        // render cells
        for (int row = 0; row < worldMap[0].length; row++) {
            for (int col = 0; col < worldMap.length; col++) {
                // fill cell
                if (worldMap[row][col] > 0) {
                    g.setColor(Color.blue);
                    g.fillRect(col * cellSize, row * cellSize, cellSize, cellSize);
                }

                // outline cell
                g.setColor(Color.black);
                g.drawRect(col * cellSize, row * cellSize, cellSize, cellSize);
            }
        }

        // render player(s)
        for (GameObject player : players) {
            player.draw(g);

            if (player instanceof Player) {
                ((Player) player).drawFOV(g);
            }
        }

    }
